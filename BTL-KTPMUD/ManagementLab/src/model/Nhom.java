package model;

public class Nhom {
	private String manhom;
	private String tennhom;
	private String huong_nc;
	private String mant;

	public String getMaNhom() {
		return manhom;
	}

	public void setMaNhom(String manhom) {
		this.manhom = manhom;
	}

	public String getTenNhom() {
		return tennhom;
	}

	public void setTenNhom(String tennhom) {
		this.tennhom = tennhom;
	}

	public String getHuong_NC() {
		return huong_nc;
	}

	public void setHuong_NC(String huong_nc) {
		this.huong_nc = huong_nc;
	}

	public String getMaNT() {
		return mant;
	}

	public void setMaNT(String mant) {
		this.mant = mant;
	}

}
