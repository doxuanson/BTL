package model;

import java.util.Date;

public class DuAn {

	private String mada;
	private String tenda;
	private String doitac;
	private Date ngaybatdau;
	private Date ngayketthuc;
	private String mota;
	private String trangthaida;

	public String getMaDA() {
		return mada;
	}

	public void setMaDA(String mada) {
		this.mada = mada;
	}

	public String getTenDA() {
		return tenda;
	}

	public void setTenDA(String tenda) {
		this.tenda = tenda;
	}

	public String getDoiTac() {
		return doitac;
	}

	public void setDoiTac(String doitac) {
		this.doitac = doitac;
	}

	public Date getNgayBatDau() {
		return ngaybatdau;
	}

	public void setNgayBatDau(Date ngaybatdau) {
		this.ngaybatdau = ngaybatdau;
	}

	public Date getNgayKetThuc() {
		return ngayketthuc;
	}

	public void setNgayKetThuc(Date ngayketthuc) {
		this.ngayketthuc = ngayketthuc;
	}

	public String getMoTa() {
		return mota;
	}

	public void setMoTa(String mota) {
		this.mota = mota;
	}

	public String getTrangThaiDA() {
		return trangthaida;
	}

	public void setTrangThaiDA(String trangthaida) {
		this.trangthaida = trangthaida;
	}

}
